package tva.how.classesFirebase;

public class OsnovePrvePomoci {

    String nazivLekcije;
    String besediloLekcije;
    String slikaUrl;
    String videoUrl;

    public OsnovePrvePomoci() {
    }

    public String getNazivLekcije() {
        return nazivLekcije;
    }

    public void setNazivLekcije(String nazivLekcije) {
        this.nazivLekcije = nazivLekcije;
    }

    public String getBesediloLekcije() {
        return besediloLekcije;
    }

    public void setBesediloLekcije(String besediloLekcije) {
        this.besediloLekcije = besediloLekcije;
    }

    public String getSlikaUrl() {
        return slikaUrl;
    }

    public void setSlikaUrl(String slikaUrl) {
        this.slikaUrl = slikaUrl;
    }

    public String getVideoUrl() {
        return videoUrl;
    }

    public void setVideoUrl(String videoUrl) {
        this.videoUrl = videoUrl;
    }
}
